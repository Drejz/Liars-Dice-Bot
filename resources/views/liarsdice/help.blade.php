    <h1><a name="_ibqvyx5upw7s"></a><span lang=EN>How-to-play:</span></h1>

    <ol style='margin-top:0cm' start=1 type=1>
        <li class=MsoNormalCxSpFirst style='mso-list:l1 level1 lfo2'><span lang=EN>Invite
     the bot to a new Slack channel</span></li>
        <li class=MsoNormalCxSpMiddle style='mso-list:l1 level1 lfo2'><span lang=EN>Invite
     people you would like to play with, to the same channel</span></li>
        <li class=MsoNormalCxSpMiddle style='mso-list:l1 level1 lfo2'><span lang=EN>Write
     “Play Liar’s” in the channel (this opens a new game)</span></li>
        <li class=MsoNormalCxSpMiddle style='mso-list:l1 level1 lfo2'><span lang=EN>Now
     other players can write “me” to join the game</span></li>
        <li class=MsoNormalCxSpMiddle style='mso-list:l1 level1 lfo2'><span lang=EN>When
     you are satisfied with the amount of players (recommended 3-5, minimum 2)
     then you write “start game” in the channel to start the game</span></li>
        <li class=MsoNormalCxSpMiddle style='mso-list:l1 level1 lfo2'><span lang=EN>Now
     the bot should write you in private, giving every player some dice, and
     the game begins.</span></li>
        <li class=MsoNormalCxSpMiddle style='mso-list:l1 level1 lfo2'><span lang=EN>The
     first player gets a message about it being their turn, and they now have
     to guess how many of a certain dice there is in the game total, you do it
     like this: “3,6”</span></li>
        <li class=MsoNormalCxSpMiddle style='mso-list:l1 level1 lfo2'><span lang=EN>If
     you write “3,6” it means you think there are at least 3 6’s in the game,
     now the next player can either write “liar” if he doesn’t believe you, or
     write “4,2”, “5,4” etc..</span></li>
        <ol style='margin-top:0cm' start=1 type=a>
            <li class=MsoNormalCxSpMiddle style='mso-list:l1 level2 lfo2'><span lang=EN>See
      special rules about the value of “1”.</span></li>
        </ol>
        <li class=MsoNormalCxSpMiddle style='mso-list:l1 level1 lfo2'><span lang=EN>If
     a player writes “liar”, the round ends, and all the winners lose a dice,
     the loser automatically starts next round again, and the goal of the game
     is to NOT have any dice once the game is over.</span></li>
        <li class=MsoNormalCxSpLast style='mso-list:l1 level1 lfo2'><span lang=EN>Rules
     are simple, there is only one loser, and a lot of winners in Liar’s Dice!</span></li>
    </ol>

